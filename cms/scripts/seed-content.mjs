#!/usr/bin/env node
/**
 * Idempotent content seed for keltus.ru.
 *
 *   DIRECTUS_URL=http://localhost:8057 \
 *   ADMIN_EMAIL=... ADMIN_PASSWORD=... \
 *   node cms/scripts/seed-content.mjs
 *
 * Run AFTER `setup-collections.mjs` has created the schema.
 *
 * Seeds:
 *   - 8 project entries (the keltus.ru portfolio + keltus.ru itself as hero)
 *   - A starter "technology" directory grouped by category
 *   - 8 team members with Russian-language bios
 *
 * Testimonials are NOT seeded — those need real photos and quotes; fill them
 * in via the Directus admin UI.
 *
 * Re-running is safe: items are matched by `slug` and PATCHed if they
 * already exist (POST if new).
 */

const URL_BASE = process.env.DIRECTUS_URL || 'http://localhost:8057';
const EMAIL    = process.env.ADMIN_EMAIL  || 'admin@keltus.ru';
const PASSWORD = process.env.ADMIN_PASSWORD || 'changeme-on-first-login';

let token = process.env.DIRECTUS_TOKEN || '';

async function login() {
  if (token) { console.log('✓ Используем DIRECTUS_TOKEN'); return; }
  const r = await fetch(`${URL_BASE}/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ email: EMAIL, password: PASSWORD }),
  });
  if (!r.ok) throw new Error(`Не удалось войти: ${r.status} ${await r.text()}`);
  token = (await r.json()).data.access_token;
  console.log(`✓ Вошли как ${EMAIL}`);
}

async function dx(method, path, body) {
  const r = await fetch(`${URL_BASE}${path}`, {
    method,
    headers: { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' },
    body: body ? JSON.stringify(body) : undefined,
  });
  if (r.status === 204) return null;
  const j = await r.json().catch(() => ({}));
  if (!r.ok) {
    const msg = j.errors?.[0]?.message || JSON.stringify(j).slice(0, 300);
    throw Object.assign(new Error(`${method} ${path} → ${r.status}: ${msg}`), { status: r.status });
  }
  return j;
}

async function upsertBySlug(collection, item) {
  const existing = await dx('GET',
    `/items/${collection}?filter[slug][_eq]=${encodeURIComponent(item.slug)}&fields=id&limit=1`);
  if (existing?.data?.[0]) {
    await dx('PATCH', `/items/${collection}/${existing.data[0].id}`, item);
    console.log(`  · обновлено ${collection}/${item.slug}`);
  } else {
    await dx('POST', `/items/${collection}`, item);
    console.log(`  + создано ${collection}/${item.slug}`);
  }
}

